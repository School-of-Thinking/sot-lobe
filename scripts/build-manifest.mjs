#!/usr/bin/env node
/**
 * Reads content/sot-knowledge/*.md, concatenates them in a defined order,
 * and writes src/lib/manifest.generated.ts as an inline TypeScript constant.
 *
 * Why a generator: Edge runtime can't read the filesystem at request time,
 * and importing .md files needs webpack/turbopack loader config. A generated
 * .ts module is the simplest portable approach.
 */
import { readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const CONTENT_DIR = join(ROOT, "content", "sot-knowledge");
const OUT_PATH = join(ROOT, "src", "lib", "manifest.generated.ts");

// Ordering: SKILL.md first (it's the entry-point with frontmatter), then docs
// from highest-level to most detail-oriented.
const ORDER = [
  "SKILL.md",
  "README.md",
  "framework.md",
  "thinking-tools.md",
  "coaching-style.md",
  "neuroscience.md",
  "curriculum.md",
  "CLIENT_TEMPLATE.md",
];

if (!existsSync(CONTENT_DIR)) {
  console.error(`[build-manifest] content dir missing: ${CONTENT_DIR}`);
  process.exit(1);
}

const sections = ORDER.map((filename) => {
  const filepath = join(CONTENT_DIR, filename);
  if (!existsSync(filepath)) {
    console.error(`[build-manifest] missing: ${filepath}`);
    process.exit(1);
  }
  return readFileSync(filepath, "utf-8").trim();
});

const manifest = sections.join("\n\n---\n\n") + "\n";
const versionStamp = new Date().toISOString();
const checksum = Buffer.from(manifest).length;

const out = `// AUTO-GENERATED by scripts/build-manifest.mjs — do not edit by hand.
// Regenerate with: npm run build:manifest
// Source: content/sot-knowledge/ (8 files, concat order in build-manifest.mjs)

export const MANIFEST_VERSION = ${JSON.stringify(versionStamp)};
export const MANIFEST_BYTES = ${checksum};

export const MANIFEST_SKILL = ${JSON.stringify(manifest)};
`;

mkdirSync(dirname(OUT_PATH), { recursive: true });
writeFileSync(OUT_PATH, out, "utf-8");

console.log(`[build-manifest] wrote ${OUT_PATH}`);
console.log(`[build-manifest] ${ORDER.length} files, ${checksum} bytes, version=${versionStamp}`);
