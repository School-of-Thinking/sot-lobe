#!/usr/bin/env node
/**
 * Reads content/scales-skill.md (the canonical SOT Universal Scales skill
 * manifest, hand-curated in this repo) and writes src/lib/manifest.generated.ts
 * as an inline TypeScript constant.
 *
 * Why a generator: Edge runtime can't read the filesystem at request time,
 * and importing .md files needs webpack/turbopack loader config. A generated
 * .ts module is the simplest portable approach.
 */
import { readFileSync, writeFileSync, mkdirSync, existsSync, statSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const SOURCE = join(ROOT, "content", "scales-skill.md");
const OUT_PATH = join(ROOT, "src", "lib", "manifest.generated.ts");

if (!existsSync(SOURCE)) {
  console.error(`[build-manifest] source missing: ${SOURCE}`);
  process.exit(1);
}

const manifest = readFileSync(SOURCE, "utf-8");
const sourceMtime = statSync(SOURCE).mtime.toISOString();
const checksum = Buffer.from(manifest).length;

const out = `// AUTO-GENERATED by scripts/build-manifest.mjs — do not edit by hand.
// Regenerate with: npm run build:manifest
// Source: content/scales-skill.md (mtime: ${sourceMtime})

export const MANIFEST_VERSION = ${JSON.stringify(sourceMtime)};
export const MANIFEST_BYTES = ${checksum};

export const MANIFEST_SKILL = ${JSON.stringify(manifest)};
`;

mkdirSync(dirname(OUT_PATH), { recursive: true });
writeFileSync(OUT_PATH, out, "utf-8");

console.log(`[build-manifest] wrote ${OUT_PATH}`);
console.log(`[build-manifest] ${checksum} bytes, source mtime=${sourceMtime}`);
